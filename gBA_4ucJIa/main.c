#include <stdio.h>
#include <stdlib.h>

int main()
{
    int x,y;

 printf("Enter the number x:");

 scanf("%d",&x);

 printf("Enter the number y:");

 scanf("%d",&y);

 printf("%d+%d=%d\n",x,y,x+y);

 printf("%d-%d=%d\n",x,y,x-y);

 printf("%d*%d=%d\n",x,y,x*y);

 printf("%d/%d=%d\n",x,y,x/y);

 printf("%d%%%d=%d\n",x,y,x%y);
return 0;
}
